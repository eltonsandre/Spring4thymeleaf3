CREATE TABLE CIDADE (
	CODIGO BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1),
	NOME VARCHAR(255) NOT NULL,
	CODIGO_ESTADO BIGINT NOT NULL, PRIMARY KEY (CODIGO)
)

CREATE TABLE ESTADO (
	CODIGO BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1),
	NOME VARCHAR(255),
	SIGLA VARCHAR(255), PRIMARY KEY (CODIGO)
)

ALTER TABLE CIDADE ADD CONSTRAINT CIDADE_ESTADO_FK FOREIGN KEY (CODIGO_ESTADO) REFERENCES ESTADO

CREATE TABLE CLIENTE (
	CODIGO BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1),
	CPF_CNPJ VARCHAR(255) NOT NULL,
	EMAIL VARCHAR(255),
	CEP VARCHAR(255),
	COMPLEMENTO VARCHAR(255),
	LOGRADOURO VARCHAR(255),
	NUMERO VARCHAR(255),
	NOME VARCHAR(255) NOT NULL,
	TELEFONE VARCHAR(255),
	TIPO_PESSOA VARCHAR(255) NOT NULL,
	CODIGO_CIDADE BIGINT, PRIMARY KEY (CODIGO)
)

ALTER TABLE CLIENTE ADD CONSTRAINT CLIENTE_CIDADE_FK FOREIGN KEY (CODIGO_CIDADE) REFERENCES CIDADE

